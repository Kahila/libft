#include "libft.h"

int			ft_count(char *s, char c)
{
	int i;
	int ci;

	if (!s || !c)
		return (0);
	i = 0;
	ci = 0;
	while (s[i++] != '\0')
	{
		if (s[i + 1] == c && s[i] != c)
			ci++;
	}
	if (ci > 0)
		ci++;
	return (ci);
}
