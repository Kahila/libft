#include "libft.h"

int				ft_isalnum(int ch)
{
	char c = (char)(ch);

	if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (8);
	else
		return (0);
}
